def compute_fbank_matrix(signal, coeff=0.97, fft_size=512, use_gpu=False):
    if use_gpu:
        signal = signal.cuda()
        povey_win_fn = POVEY_WIN_FN.cuda()
        mel_scale = MEL_SCALE.cuda()
    else:
        povey_win_fn = POVEY_WIN_FN
        mel_scale = MEL_SCALE
    batch_size = signal.shape[0]

    # padding wavform
    padding_head = torch.zeros([batch_size, 240], dtype=torch.float, 
                               device=signal.device)
    # padding_rear = torch.zeros([batch_size, 400], dtype=torch.float, 
    #                            device=signal.device)
    signal = torch.cat([padding_head,signal],dim=-1)
    signal_len = signal.shape[-1]

    # split frame, 分帧
    frame_index = 0
    frame_num = int(((signal_len-WIN_LEN)/WIN_SHIFT)+1)
    frames_matrix = torch.zeros((batch_size, frame_num, WIN_LEN), 
                                device=signal.device)
    
    # signal = torch.cat([signal,padding_rear],dim=-1)
    while True:
        if (frame_index * WIN_SHIFT + WIN_LEN) > signal_len:
            break
        frames_matrix[:, frame_index, :] = signal[:, frame_index*160: 
                                                  frame_index*160+400].clone()
        frame_index += 1

    # dc remove, 减均值
    frames_matrix -= frames_matrix.mean(dim=-1, keepdim=True)

    # pre-emphasis, 预加重
    frames_matrix[:,:,0] = frames_matrix[:,:,0] - frames_matrix[:,:,0] * coeff
    frames_matrix[:,:,1:] -= coeff * frames_matrix[:,:,:-1]

    # window, 加窗
    win_frame = frames_matrix * povey_win_fn

    # fft, 快速离散傅里叶变换
    fft_256_frame = torch.fft.fft(win_frame, fft_size, dim=-1)[:,:,:256]
    freq_power = fft_256_frame.abs().pow(2.0)

    # mel-filtering, mel滤波
    fbank_buf = torch.matmul(freq_power, mel_scale.t())

    # log, 取对数
    fbank = fbank_buf + 1e-6
    fbank = fbank.log()

    return fbank



